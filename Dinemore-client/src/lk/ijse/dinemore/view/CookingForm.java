    private void buttonCookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCookingActionPerformed
        try {
           OrderDTO orderDTO=new OrderDTO(
            txtcidCook.getText(),
            txtNameCook.getText(), 
            txtTelCook.getText(),
            Integer.parseInt(txtQtyCook.getText()),
             "COOKING"
            );
            boolean updateCustomer = ManageOrderController.updateCustomer(orderDTO);
            if (updateCustomer) {
                JOptionPane.showMessageDialog(this, "Customer has been Update Successfully");
                loadAllOrders();
                clearText();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Update Customer");
            }
            ManageOrderController.releaseCustomer(reservedCustomerID);
            reservedCustomerID="";
        } catch (Exception ex) {
            Logger.getLogger(CookingForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_buttonCookingActionPerformed

