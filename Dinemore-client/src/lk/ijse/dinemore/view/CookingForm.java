    private void tableCookingMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCookingMouseClicked
       DefaultTableModel dtm = (DefaultTableModel) tableCooking.getModel();
        int selectedRow = tableCooking.getSelectedRow();
        String id = dtm.getValueAt(selectedRow, 0).toString();
        String name = dtm.getValueAt(selectedRow, 1).toString();
        String tele = dtm.getValueAt(selectedRow, 2).toString();
        String qty = dtm.getValueAt(selectedRow, 3).toString();
        String creditLimt = dtm.getValueAt(selectedRow, 4).toString();
        txtcidCook.setText(id);
        txtNameCook.setText(name);
        txtTelCook.setText(tele);
        txtQtyCook.setText(qty);
        txtStatusCook.setText(creditLimt);
    }//GEN-LAST:event_tableCookingMouseClicked
private void loadAllOrders() {
        DefaultTableModel dtm = (DefaultTableModel) tableCooking.getModel();
        dtm.setRowCount(0);
        try {
            List<OrderDTO> customers = ManageOrderController.getAllOrders();
            if (customers == null) {
                return;
            }

            for (OrderDTO customer : customers) {
                Object[] rowData = {
                    customer.getOid(),
                    customer.getName(),
                    customer.getTele(),
                    customer.getQty(),
                    customer.getStatus()
                };
                dtm.addRow(rowData);
            }

        } catch (Exception ex) {
            Logger.getLogger(ManageOrder.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
@Override
    public void updateObservers() throws Exception {
        loadAllOrders();
    }
    
